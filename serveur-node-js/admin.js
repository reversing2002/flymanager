// Middleware pour v√©rifier le r√¥le admin
const checkAdminRole = async (req, res, next) => {
  try {
    const authHeader = req.headers.authorization;
    if (!authHeader || !authHeader.startsWith('Bearer ')) {
      return res.status(401).json({ error: 'Token d\'authentification manquant' });
    }

    const token = authHeader.split(' ')[1];
    const { data: { user }, error } = await adminClient.auth.getUser(token);
    
    if (error) {
      console.error('Erreur lors de la v√©rification du token:', error);
      return res.status(401).json({ error: 'Token invalide' });
    }

    if (!user) {
      return res.status(401).json({ error: 'Utilisateur non trouv√©' });
    }

    // V√©rifier le r√¥le admin
    const { data: roles, error: rolesError } = await adminClient
      .from('user_group_memberships')
      .select('user_groups (code)')
      .eq('user_id', user.id);

    if (rolesError) {
      console.error('Erreur lors de la v√©rification des r√¥les:', rolesError);
      return res.status(500).json({ error: 'Erreur lors de la v√©rification des r√¥les' });
    }

    const isAdmin = roles?.some(membership => membership.user_groups?.code === 'ADMIN');
    if (!isAdmin) {
      return res.status(403).json({ error: 'Acc√®s non autoris√©' });
    }

    req.user = user;
    next();
  } catch (error) {
    console.error('Erreur dans le middleware admin:', error);
    res.status(500).json({ error: 'Erreur interne du serveur' });
  }
};

