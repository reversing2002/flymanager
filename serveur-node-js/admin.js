function generateRandomPassword() {
  const length = 16;
  const charset = 'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789!@#$%^&*()_+-=[]{}|;:,.<>?';
  let password = '';
  for (let i = 0; i < length; i++) {
    password += charset[Math.floor(Math.random() * charset.length)];
  }
  return password;
}

function slugify(str, options = {}) {
  const defaults = {
    replacement: '-',
    lower: false,
    strict: false,
    trim: true,
    charmap: {}
  };

  options = Object.assign({}, defaults, options);

  let result = '';
  const regex = /[\u{0080}-\u{FFFF}]/gu;

  if (options.trim) {
    str = str.trim();
  }

  if (options.lower) {
    str = str.toLowerCase();
  }

  if (options.strict) {
    str = str.replace(/[^a-z0-9]/g, options.replacement);
  } else {
    str = str.replace(regex, match => {
      const char = match.charCodeAt(0);
      if (char >= 128) {
        return options.replacement;
      }
      return match;
    });
  }

  for (const char in options.charmap) {
    str = str.replace(new RegExp(char, 'g'), options.charmap[char]);
  }

  result = str;

  return result;
}

